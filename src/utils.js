function getKeyObject(key) {
    var k = new Date(key)
    k = `day-${k.getDate()}`
    return k
}

function groupByDate(data) {
    var groupDatas = {}
    for (var it = 0; it < data.length; it++) {
        var item = data[it];
        var key = getKeyObject(item.dt_txt)
        if (!groupDatas[key])
            groupDatas[key] = [];

        groupDatas[key].push(item);
    }
    return groupDatas

}

